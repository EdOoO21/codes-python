for i in range(10):
    a = 9 + 7 * 10 + i * (10**2) + 5 * (10**3) + 4 * (10**4) + 2 * (10**5) + 1 * (10**6)
    i1 = a
    s = 0
    s1 = 0
    while i1 > 0:
        if (i1 % 10) % 2 == 1:
            s += (i1 % 10)
        s1 += i1 % 10
        i1 //= 10
    if a % s == 0:
        print(a,s1)

